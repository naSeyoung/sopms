package sopms.vo;

public class CalList {
	private int no;
	private String title;
	private int d_day;
	private String manager;
	private String cal_name;
	private String cal_process;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getD_day() {
		return d_day;
	}
	public void setD_day(int d_day) {
		this.d_day = d_day;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getCal_name() {
		return cal_name;
	}
	public void setCal_name(String cal_name) {
		this.cal_name = cal_name;
	}
	public String getCal_process() {
		return cal_process;
	}
	public void setCal_process(String cal_process) {
		this.cal_process = cal_process;
	};
	
}
